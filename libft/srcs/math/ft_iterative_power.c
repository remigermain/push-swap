#include "libft.h"

int	ft_iterative_power(int nb, int power)
{
	int result;
	int a;

	a = power;
	result = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	if (power == 1)
		return (nb);
	while (a-- != 1)
		result = result * nb;
	return (result);
}
