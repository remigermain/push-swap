char		*ft_strrev(char *str)
{
	int		a;
	int		b;
	char	c;

	a = 0;
	b = 0;
	while (str[a] != '\0')
		a++;
	a = a - 1;
	while (a > b)
	{
		c = str[a];
		str[a] = str[b];
		str[b] = c;
		b++;
		a--;
	}
	return (str);
}
